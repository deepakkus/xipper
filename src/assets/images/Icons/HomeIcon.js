export const DynamicStars = ({ rating }) => {

  const { selectedProfile } = useSelector((state) => state.account);

  // Determine the color based on the profile type
  const starColor =
    selectedProfile.type === 'user'
      ? '#06A77D' // Green for 'user'
      : '#6D38C3'; // Purple for 'company'
  if (rating < 1 || rating > 5) {
    return null; // or return a default star icon if desired
  }

  switch (rating) {
    case 1:
      return <OneStarIcon fill={starColor}/>;
    case 2:
      return <TwoStarIcon fill={starColor}/>;
    case 3:
      return <ThreeStarIcon fill={starColor}/>;
    case 4:
      return <FourStarIcon fill={starColor}/>;
    case 5:
      return <FiveStarIcon fill={starColor}/>;
    default:
      return null; // This will never hit due to the earlier check
  }
};

